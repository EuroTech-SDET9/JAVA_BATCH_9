package days.day10;

public class RelationalOps {
    public static void main(String[] args) {
        //  ==
        System.out.println(10 == 15);//boolean value false
        // > < >= <=
        System.out.println(10 > 5);// true
        int a = 10;
        int b = 15;
        System.out.println(a != b);// true
        System.out.println("a<=b = " + (a <= b));


    }
}
