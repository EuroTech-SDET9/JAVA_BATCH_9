package days.day8;

public class AssignmentsOpt {

    public static void main(String[] args) {

        System.out.println("/////  += ////////////");

        int num1= 10;
        int num2 = 20;

        num2 += num1;  //  num2 = num2 + num1;
        System.out.println("num2 = " + num2);

        System.out.println("\n/////  -= ////////////");

        num1 = 20;
        num2 = 50;

        num2 -= num1;  // num2 = num2-num1;
        System.out.println("num2 = " + num2);

        System.out.println("\n/////  *= ////////////");

        num1 = 3;
        num2 = 7;

        num2 *= num1; // num2 = num2 * num1;
        System.out.println("num2 = " + num2);

        System.out.println("\n/////  /= ////////////");

        num1 = 5;
        num2 = 20;

        num2 /= num1; // num2 = num2 / num1;
        System.out.println("num2 = " + num2);


    }
}
