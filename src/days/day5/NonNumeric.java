package days.day5;

public class NonNumeric {
    public static void main(String[] args) {
        // boolean 1 bit       char 2 bytes
        boolean isSuccess;
        isSuccess = true;
        isSuccess = false;
        System.out.println("isSuccess = " + isSuccess);
        char grade = 'A';
        grade = 'B';
        System.out.println("grade = " + grade);
        grade = 66;
        System.out.println("grade = " + grade);
        grade = 65;
        System.out.println("grade = " + grade);
        grade = 97;
        System.out.println("grade = " + grade);
        grade = 116;
        System.out.println("grade = " + grade);
        int num = 'A';
        System.out.println("num = " + num);


    }
}
