package days.day5;

public class NamingRules {
    public static void main(String[] args) {
        String name;
        // we cant use special characters while naming variables except $ and _
        // we can not use number as first letter of the variable
        // we cant use reserved key words
        // we cant use white space in the name
        // descriptive names
        // camel case
        String myName;
        final double pi = 3.14;
        // pi = 3.143;   i cant change value because it is final

        //  Ali said "Hello"
        // escape sequences
        System.out.println("Ali said \"Hello\"");
        System.out.println("hello guys \n how are you");
        System.out.println("Ali\tVeli");
        System.out.println("Ali\bVeli");
        System.out.println("my grade is 'A'");
        System.out.println("hello /\\");
        System.out.println("hello \\\\");

    }
}
