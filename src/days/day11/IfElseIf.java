package days.day11;

public class IfElseIf {
    public static void main(String[] args) {
        // if statements
        // if else
        // nested if
        int number = 30;

        if (number == 10) {
            System.out.println("number  is ten .");
        } else if (number == 20) {
            System.out.println("number is 20 ");
        } else if (number == 30) {
            System.out.println("number is 30 ");
        } else if (number == 40) {
            System.out.println("number is 40 ");
        } else {
            System.out.println("number is not 10,20,30 and 40");
        }

        System.out.println("out of the if statements");

    }
}
